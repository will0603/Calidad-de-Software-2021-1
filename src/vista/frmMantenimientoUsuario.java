    private boolean validar() {
        boolean resultado = false;
        if (
            this.txtUsuario.getText().length()!= 0 &&
            this.txtContrase単a.getText().length()!= 0 &&
            this.txtEmail.getText().length()!= 0 &&
            this.cboPerfil.getSelectedIndex() != -1
            ){
            resultado = true;
        }
        return resultado;
    }
    
